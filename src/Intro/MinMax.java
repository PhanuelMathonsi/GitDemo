package Intro;

public class MinMax {

	public static void main(String[] args) {

		System.out.println("The minimum value for an integer is: " + Integer.MIN_VALUE);
		System.out.println("The maximum value for an integer is: " + Integer.MAX_VALUE);
		
		System.out.println("The minimum value for Byte is: " + Byte.MIN_VALUE);
		System.out.println("The maximum value for Byte is: " + Byte.MAX_VALUE);
		
		System.out.println("The minimum value for Byte is: " + Float.MIN_VALUE);
		System.out.println("The maximum value for Byte is: " + Float.MAX_VALUE);
		
	}

}
